// Get current language from localStorage or default to 'nl'
export const getCurrentLanguage = () => {
  return localStorage.getItem('language') || 'nl'
}

// Set current language
export const setCurrentLanguage = (lang) => {
  localStorage.setItem('language', lang)
  window.dispatchEvent(new Event('languagechange'))
}

// Translate function with params support
export const t = (key, params = {}, lang = null) => {
  const currentLang = lang || getCurrentLanguage()
  let value = translations[currentLang]?.[key]
  
  // Fallback to Dutch if translation not found
  if (!value) {
    value = translations.nl?.[key]
  }
  
  // Simple placeholder replacement
  if (typeof value === 'string' && Object.keys(params).length > 0) {
    return value.replace(/\{(\w+)\}/g, (match, paramKey) => {
      return params[paramKey] !== undefined ? params[paramKey] : match
    })
  }
  
  return value || key
}

// Get all translations for a key
export const getAllTranslations = (key) => {
  return {
    nl: t(key, {}, 'nl'),
    ar: t(key, {}, 'ar'),
    en: t(key, {}, 'en'),
    fr: t(key, {}, 'fr')
  }
}

export default {
  getCurrentLanguage,
  setCurrentLanguage,
  t,
  getAllTranslations
}
